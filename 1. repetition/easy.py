a = int(input())
b = int(input())

# Сумма чисел a и b
sm = a + b
print(sm)

# Разность чисел a и b
diff = a - b
print(diff)

# Произведение чисел a и b
product = a * b
print(product)

# Частное чисел a и b
quotient = a / b
print(quotient)

# Целая часть от деления числа a на b
integer_division = a // b
print(integer_division)

# Остаток от деления числа a на b
remainder = a % b
print(remainder)

# Корень квадратный из суммы их 10-х степеней
sqrt_sum_pow_10 = (a**10 + b**10)**0.5
print(sqrt_sum_pow_10)
